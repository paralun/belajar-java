package com.paralun.basic;

public class OperasiPerbandingan {
    public static void main(String[] args) {
        var a = 40;
        var b = 30;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);

    }
}
